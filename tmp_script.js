    function t(key, lang) {
      return translations[lang]?.[key] || translations['en']?.[key] || key;
    }

    function renderPage(lang) {
      document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        el.innerHTML = t(key, lang);
      });
    }

    function switchLang(lang) {
      currentLanguage = translations[lang] ? lang : 'en';
      renderPage(currentLanguage);
      document.documentElement.lang = currentLanguage;
      localStorage.setItem('carbonpocket-lang', currentLanguage);
      document.querySelectorAll('.lang-bar span').forEach((pill) => {
        pill.classList.toggle('active', pill.dataset.lang === currentLanguage);
      });
      const ps = document.getElementById('paygo-slider');
      if (ps) updatePaygo(ps.value);
    }

